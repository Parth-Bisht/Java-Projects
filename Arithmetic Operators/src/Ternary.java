public class Ternary {
    public static void main(String[] args){
        //Binary Operator
        // 5+5, 4*5 which have two operands

        // Uniary Operator
        // boolean x = !(4>5) which have single operand

        //ternary Operator: Only one ternary operator in java
        boolean x;
        x = (5>4)?true:false;
        System.out.println(x);

        int a ;
        a = (10>9)?1:0;
        System.out.println(a);
    }
}
